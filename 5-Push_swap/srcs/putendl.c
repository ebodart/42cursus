#include "../includes/push_swap.h"

void	ft_putendl(char *s, int fd)
{
	int	i;

	i = 0;
	while (!(!s) && s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
